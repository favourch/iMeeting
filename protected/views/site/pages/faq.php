<p class="faq-title "><![if !supportLists]>5.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <![endif]>
	<a href="#Tap-am-va-tieng-vong">Tại sao khi sử dụng loa ngoài hay bị tạp âm và tiếng vọng?</a></p>
<p class="faq-item">Khi sử dụng loa ngoài, một phần âm thanh phát ra từ loa sẽ được micro thu 
