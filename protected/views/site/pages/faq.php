<p class="faq-title "><![if !supportLists]>8.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <![endif]>
	<a href="#Toc-do-duong-truyen">Tốc độ đường truyền Internet như thế nào thì có thể sử dụng tốt iMeeting?</a></p>
<p class="faq-item">Khi chia sẻ webcam và micro, ta đã sử dụng khoảng 50 KBps cho mỗi người dùng. 
